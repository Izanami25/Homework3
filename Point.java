package com.company;

public class Point {
    protected int x;
    protected int y;
    public Point (){
        this.x = x;
        this.y = y;
    }
    public void setX(int x){
        this.x = x;
    }
    public void setY(int y){
        this.y = y;
    }
    int getX(){
        return x;
    }

    int getY() {
        return y;
    }
}
